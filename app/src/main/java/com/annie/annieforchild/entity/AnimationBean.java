package com.annie.annieforchild.entity;

public class AnimationBean {
    private int animationId;
    private String animationName;
    private String animationImageUrl;
    private String aniamtionUrl;
    private int jurisdiction;

    public int getAnimationId() {
        return animationId;
    }

    public void setAnimationId(int animationId) {
        this.animationId = animationId;
    }

    public String getAnimationName() {
        return animationName;
    }

    public void setAnimationName(String animationName) {
        this.animationName = animationName;
    }

    public String getAnimationImageUrl() {
        return animationImageUrl;
    }

    public void setAnimationImageUrl(String animationImageUrl) {
        this.animationImageUrl = animationImageUrl;
    }

    public String getAniamtionUrl() {
        return aniamtionUrl;
    }

    public void setAniamtionUrl(String aniamtionUrl) {
        this.aniamtionUrl = aniamtionUrl;
    }

    public int getJurisdiction() {
        return jurisdiction;
    }

    public void setJurisdiction(int jurisdiction) {
        this.jurisdiction = jurisdiction;
    }
}
